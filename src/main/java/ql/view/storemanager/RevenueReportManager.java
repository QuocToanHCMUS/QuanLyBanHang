    private void setInvoiceList(List<Invoice> invoiceList) {
        this.invoiceList = invoiceList;
        
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        modelTable.setRowCount(0);
        double totalAmount = 0;
        for (Invoice invoice : invoiceList) {
            Staff staff = (Staff) invoice.getUser();
            double discountAmount = invoice.getDiscountAmount();
            totalAmount += discountAmount;
            String[] row = new String[] {
                invoice.getId(),
                staff.getStaffNo(),
                staff.getName(),
                Utils.formatDate(invoice.getSaleDate()),
                discountAmount > 0 ? Utils.formatCurrency(invoice.getDiscountAmount()) : "0"
            };
            modelTable.addRow(row);
        }
        jLabel6.setText(totalAmount != 0 ? Utils.formatCurrency(totalAmount) : "0");
    }
