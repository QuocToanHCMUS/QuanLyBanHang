    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        final JFileChooser saveAs = new JFileChooser();
        saveAs.setApproveButtonText("Save");
        int actionDialog = saveAs.showOpenDialog(this);
        if (actionDialog != JFileChooser.APPROVE_OPTION) {
            return;
        }

        PrintController printController = new PrintController();
        File file = new File(saveAs.getSelectedFile() + ".xlsx");
        boolean result = printController.printReportRevenueExcel(file, this.invoiceList);
        if (result) {
            JOptionPane.showMessageDialog(rootPane, "Lưu doanh thu file thành công, path: " + file.getAbsolutePath());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra. Vui lòng thử lại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


