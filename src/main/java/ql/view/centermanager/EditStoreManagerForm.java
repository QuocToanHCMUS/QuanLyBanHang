    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField2.getText();
        String name = jTextField3.getText();
        Date birthday = jDateChooser1.getDate();
        String address = jTextField5.getText();
        String storeName = (String) jComboBox1.getSelectedItem();
        String storeId = this.storeList.stream().filter(s -> s.getName().equalsIgnoreCase(storeName)).findFirst().get().getId();

        UserController userController = new UserController();
        boolean result = userController.update(username, name, birthday, address, storeId);
        if (result) {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật nhân viên thành công");
            List<User> storeManagerList = userController.getAllStoreManagerBy("", "");
            this.accountManagerForm.setUserList(storeManagerList);
            this.accountManagerForm.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật nhân viên thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

