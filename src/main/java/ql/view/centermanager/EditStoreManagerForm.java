    public void setData(User user, List<Store> storeList, AccountManagerForm accountManagerForm) {
        this.accountManagerForm = accountManagerForm;
        this.storeList = storeList;
        
        jTextField2.setText(user.getUsername());
        jTextField3.setText(user.getName());
        jDateChooser1.setDate(user.getBirthday());
        jTextField5.setText(user.getAddress());
        
        for (Store store : storeList) {
            jComboBox1.addItem(store.getName());
        }
        
        jComboBox1.setSelectedItem(user.getStore().getName());
    }
