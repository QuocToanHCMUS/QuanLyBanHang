    private void setStoreList(List<Store> storeList) {
        this.storeList = storeList;
        
        jComboBox1.addItem("Tất cả");
        for (Store store : storeList) {
            jComboBox1.addItem(store.getName());
        }
    }

    public void setUserList(List<User> userList) {
        this.userList = userList;
        
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        modelTable.setRowCount(0);
        int stt = 1;
        for (User user : userList) {
            String[] row = new String[] {
                String.valueOf(stt++),
                user.getName(),
                String.valueOf(user.getBirthday()),
                user.getAddress(),
                user.getUsername(),
                user.getStore().getName()
            };
            stt++;
            modelTable.addRow(row);
        }
    }
