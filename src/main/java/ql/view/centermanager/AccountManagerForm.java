    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String storeName = (String) jComboBox1.getSelectedItem();
        Optional<Store> optStore = this.storeList.stream().filter(s -> s.getName().equalsIgnoreCase(storeName)).findFirst();
        String storeId = optStore.isPresent() ? optStore.get().getId() : null;
        UserController userController = new UserController();
        List<User> userList = userController.getAllStoreManagerBy(storeId, name);
        setUserList(userList);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (username == null) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn một quản lý cửa hàng");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa quản lý cửa hàng này không?");
        if (confirm == 0) {
            UserController userController = new UserController();
            boolean result = userController.removeStoreManager(username);
            if (result) {
                JOptionPane.showMessageDialog(rootPane, "Xóa quản lý cửa hàng thành công");
                List<User> userList = userController.getAllStoreManagerBy("", "");
                setUserList(userList);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Xóa quản lý cửa hàng thất bại");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

