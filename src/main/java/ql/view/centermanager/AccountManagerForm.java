    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (username == null) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn một quản lý cửa hàng");
            return;
        }
        EditStoreManagerForm editStoreManagerForm = new EditStoreManagerForm();
        User user = this.userList.stream().filter(u -> username.equals(u.getUsername())).findFirst().get();
        editStoreManagerForm.setData(user, storeList, this);
        editStoreManagerForm.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

