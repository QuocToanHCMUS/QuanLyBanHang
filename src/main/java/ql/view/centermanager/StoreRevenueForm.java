    private void setStoreList(List<Store> storeList) {
        this.storeList = storeList;
        
        jComboBox1.addItem("Tất cả");
        for (Store store : storeList) {
            jComboBox1.addItem(store.getName());
        }
    }

    private void setInvoiceList(List<RevenueStoreDto> invoices) {
        this.invoiceList = invoices;
        
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        modelTable.setRowCount(0);
        double totalAmount = 0;
        int stt = 1;
        for (RevenueStoreDto invoice : invoices) {
            double discountAmount = invoice.getTotalAmount();
            totalAmount += discountAmount;
            String[] row = new String[] {
                String.valueOf(stt),
                invoice.getStoreName(),
                invoice.getStoreAddress(),
                discountAmount > 0 ? Utils.formatCurrency(discountAmount) : "0"
            };
            stt++;
            modelTable.addRow(row);
        }
        jLabel6.setText(totalAmount != 0 ? Utils.formatCurrency(totalAmount) : "0");
    }
