    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String storeName = (String) jComboBox1.getSelectedItem();
        Optional<Store> optStore = this.storeList.stream().filter(s -> s.getName().equalsIgnoreCase(storeName)).findFirst();
        String storeId = optStore.isPresent() ? optStore.get().getId() : "";
        Date startDate = jDateChooser1.getDate();
        Date endDate = jDateChooser2.getDate();
        
        InvoiceController invoiceController = new InvoiceController();
        List<RevenueStoreDto> invoices = invoiceController.revenue(storeId, startDate, endDate);
        setInvoiceList(invoices);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        final JFileChooser saveAs = new JFileChooser();
        saveAs.setApproveButtonText("Save");
        int actionDialog = saveAs.showOpenDialog(this);
        if (actionDialog != JFileChooser.APPROVE_OPTION) {
            return;
        }

        PrintController printController = new PrintController();
        File file = new File(saveAs.getSelectedFile() + ".xlsx");
        boolean result = printController.printStoreRevenueExcel(file, invoiceList);
        if (result) {
            JOptionPane.showMessageDialog(rootPane, "Lưu doanh thu cửa hàng ra file thành công, path: " + file.getAbsolutePath());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra. Vui lòng thử lại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


